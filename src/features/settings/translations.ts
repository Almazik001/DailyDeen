const dictionaries: Record<LanguageMode, Dict> = {
  english,
  russian,
  kazakh,
}

export function t(
  language: LanguageMode,
  key: string,
  values?: Record<string, string>,
) {
  const template = dictionaries[language][key] ?? dictionaries.english[key] ?? key

  if (!values) {
    return template
  }

  return Object.entries(values).reduce(
    (result, [name, value]) => result.replace(`{${name}}`, value),
    template,
  )
}

export function getSidebarLabel(language: LanguageMode, view: AppView) {
  return t(language, `sidebar.${view}`)
}
